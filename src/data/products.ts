// Price multipliers for different regions (Lebanon is base price)
const regionMultipliers: { [key: string]: number } = {
  lebanon: 1,
  bamburi: 1.05,
  mombasa: 1.05,
  malindi: 1.1,
  kilifi: 1.08,
  nyali: 1.02,
  likoni: 1.03,
  kisauni: 1.04,
  mvita: 1,
  changamwe: 1.02,
};

// Helper function to generate prices for all regions based on base price
const generatePrices = (basePrice: number): { [region: string]: number } => {
  const prices: { [region: string]: number } = {};
  Object.keys(regionMultipliers).forEach((region) => {
    prices[region] = Math.round(basePrice * regionMultipliers[region]);
  });
  return prices;
};

